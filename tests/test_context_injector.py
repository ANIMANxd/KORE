class TestExportContextFile:
    @patch("integrations.context_injector.load_memory_store")
    @patch("integrations.context_injector.query_memory")
    def test_writes_file_and_returns_path(self, mock_query, mock_load, tmp_path):
        store = MemoryStore(
            entries=[
                MemoryEntry(
                    content="Refund within 7 days",
                    entry_type="rule",
                    confidence=0.95,
                    freshness=0.92,
                    embedding=[0.1, 0.2],
                    source_ids=["rule-1"],
                )
            ]
        )
        mock_load.return_value = store
        mock_query.return_value = store.entries

        out = tmp_path / "context.md"
        path = export_context_file("refund policy", output_path=str(out), output_format="markdown")
        assert Path(path).exists()
        content = Path(path).read_text(encoding="utf-8")
        assert "Refund within 7 days" in content
        assert "# Company Knowledge Context" in content

    @patch("integrations.context_injector.load_memory_store")
    @patch("integrations.context_injector.query_memory")
    def test_creates_parent_dirs(self, mock_query, mock_load, tmp_path):
        store = MemoryStore(
            entries=[
                MemoryEntry(
                    content="Test",
                    entry_type="rule",
                    confidence=0.9,
                    freshness=0.9,
                    embedding=[0.1],
                )
            ]
        )
        mock_load.return_value = store
        mock_query.return_value = store.entries

        out = tmp_path / "deep" / "nested" / "context.md"
        path = export_context_file("test", output_path=str(out))
        assert Path(path).exists()
