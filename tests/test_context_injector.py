class TestFormatEntryMarkdown:
    def test_includes_confidence_and_freshness(self):
        entry = MemoryEntry(
            content="Refund within 7 days",
            entry_type="rule",
            confidence=0.95,
            freshness=0.92,
            source_ids=["rule-1"],
        )
        lines = _format_entry_markdown(1, entry)
        text = "\n".join(lines)
        assert "### [1] RULE" in text
        assert "0.95" in text
        assert "0.92" in text
        assert "rule-1" in text
        assert "Refund within 7 days" in text


class TestFormatEntryXml:
    def test_includes_cdata_and_attributes(self):
        entry = MemoryEntry(
            content="Special <chars> & stuff",
            entry_type="rule",
            confidence=0.95,
            freshness=0.92,
            source_ids=["rule-1"],
        )
        lines = _format_entry_xml(1, entry)
        text = "\n".join(lines)
        assert '<entry index="1" type="rule">' in text
        assert "<confidence>0.95</confidence>" in text
        assert "<![CDATA[Special <chars> & stuff]]>" in text


