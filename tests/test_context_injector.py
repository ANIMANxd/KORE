class TestWrapContext:
    def test_markdown_header(self):
        result = _wrap_context(["line"], "markdown", "pricing")
        assert "# Company Knowledge Context — pricing" in result
        assert "Auto-generated by KORE" in result

    def test_xml_header(self):
        result = _wrap_context(["  <entry/>"], "xml", "pricing")
        assert '<?xml version="1.0"' in result
        assert '<company-knowledge domain="pricing"' in result

    def test_plain_header(self):
        result = _wrap_context(["line"], "plain", "pricing")
        assert "COMPANY KNOWLEDGE CONTEXT — pricing" in result


